let billingStorageReady: Promise<void> | null = null;

function isoOrNull(value: string | Date | null | undefined): string | null {
  if (!value) return null;
  return value instanceof Date ? value.toISOString() : value;
}

function isAlreadyExistsError(err: unknown): boolean {
  const message = err instanceof Error ? err.message : String(err);
  return /ORA-00955|ORA-01408|ORA-02261/.test(message);
}

async function execOracleBillingDdl(sql: string): Promise<void> {
  try {
    await asyncDb.exec(sql);
  } catch (err) {
    if (isAlreadyExistsError(err)) return;
    throw err;
  }
}

async function ensureOracleBillingStorage(): Promise<void> {
  await execOracleBillingDdl(`
    CREATE TABLE billing_customers (
      id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
      user_id NUMBER(19,0) NOT NULL,
      stripe_customer_id VARCHAR2(255 CHAR) NOT NULL,
      created_at TIMESTAMP(6) DEFAULT CURRENT_TIMESTAMP,
      updated_at TIMESTAMP(6) DEFAULT CURRENT_TIMESTAMP,
      CONSTRAINT pk_billing_customers PRIMARY KEY (id),
      CONSTRAINT uq_billing_customers_user UNIQUE (user_id),
      CONSTRAINT uq_billing_customers_stripe UNIQUE (stripe_customer_id),
      CONSTRAINT fk_billing_customers_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
    )
  `);

  await execOracleBillingDdl(`
    CREATE TABLE billing_subscriptions (
      id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
      user_id NUMBER(19,0) NOT NULL,
      billing_customer_id NUMBER(19,0) NOT NULL,
      stripe_customer_id VARCHAR2(255 CHAR) NOT NULL,
      stripe_subscription_id VARCHAR2(255 CHAR) NOT NULL,
      status VARCHAR2(64 CHAR) NOT NULL,
      plan_key VARCHAR2(64 CHAR) DEFAULT 'free' NOT NULL,
      stripe_price_id VARCHAR2(255 CHAR),
      current_period_start VARCHAR2(4000 CHAR),
      current_period_end VARCHAR2(4000 CHAR),
      trial_start VARCHAR2(4000 CHAR),
      trial_end VARCHAR2(4000 CHAR),
      cancel_at_period_end NUMBER(19,0) DEFAULT 0 NOT NULL,
      canceled_at VARCHAR2(4000 CHAR),
      created_at TIMESTAMP(6) DEFAULT CURRENT_TIMESTAMP,
      updated_at TIMESTAMP(6) DEFAULT CURRENT_TIMESTAMP,
      CONSTRAINT pk_billing_subscriptions PRIMARY KEY (id),
      CONSTRAINT uq_billing_subscriptions_stripe UNIQUE (stripe_subscription_id),
      CONSTRAINT fk_billing_subscriptions_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
      CONSTRAINT fk_billing_subscriptions_customer FOREIGN KEY (billing_customer_id)
        REFERENCES billing_customers(id) ON DELETE CASCADE
    )
  `);

  await execOracleBillingDdl(`
    CREATE INDEX idx_billing_subscriptions_user ON billing_subscriptions(user_id)
  `);
  await execOracleBillingDdl(`
    CREATE INDEX idx_billing_subscriptions_customer ON billing_subscriptions(stripe_customer_id)
  `);
  await execOracleBillingDdl(`
    CREATE INDEX idx_billing_subscriptions_status ON billing_subscriptions(status)
  `);

  await execOracleBillingDdl(`
    CREATE TABLE billing_webhook_events (
      id VARCHAR2(255 CHAR) NOT NULL,
      type VARCHAR2(255 CHAR) NOT NULL,
      stripe_created NUMBER(19,0),
      processed_at TIMESTAMP(6) DEFAULT CURRENT_TIMESTAMP,
      CONSTRAINT pk_billing_webhook_events PRIMARY KEY (id)
    )
  `);

  await execOracleBillingDdl(`
    CREATE INDEX idx_billing_webhook_events_processed_at ON billing_webhook_events(processed_at)
  `);
}

async function ensureBillingStorage(): Promise<void> {
  if (resolveDbProvider() !== 'oracle-async') return;
  if (!billingStorageReady) {
    billingStorageReady = ensureOracleBillingStorage().catch((err) => {
      billingStorageReady = null;
      throw err;
    });
  }
  await billingStorageReady;
}

export async function getBillingCustomerForUser(userId: number): Promise<BillingCustomer | undefined> {
  await ensureBillingStorage();
  return asyncDb.prepare('SELECT * FROM billing_customers WHERE user_id = ?').get<BillingCustomer>(userId);
}

export async function getBillingCustomerByStripeId(stripeCustomerId: string): Promise<BillingCustomer | undefined> {
  await ensureBillingStorage();
  return asyncDb
    .prepare('SELECT * FROM billing_customers WHERE stripe_customer_id = ?')
    .get<BillingCustomer>(stripeCustomerId);
}

export async function userExists(userId: number): Promise<boolean> {
  return !!(await asyncDb.prepare('SELECT id FROM users WHERE id = ?').get<{ id: number }>(userId));
}

export async function upsertBillingCustomer(userId: number, stripeCustomerId: string): Promise<BillingCustomer> {
  await ensureBillingStorage();
  await asyncDb
    .prepare(
      `
      INSERT INTO billing_customers (user_id, stripe_customer_id, updated_at)
      VALUES (?, ?, CURRENT_TIMESTAMP)
      ON CONFLICT(user_id) DO UPDATE SET
        stripe_customer_id = excluded.stripe_customer_id,
        updated_at = CURRENT_TIMESTAMP
    `,
    )
    .run(userId, stripeCustomerId);

  const customer = await getBillingCustomerForUser(userId);
  if (!customer) throw new Error('Failed to persist billing customer.');
  return customer;
}

