export async function markStripeSubscriptionPaymentFailed(
  stripeSubscriptionId: string,
): Promise<BillingSubscription | undefined> {
  const existing = await getBillingSubscriptionByStripeId(stripeSubscriptionId);
  if (!existing) return undefined;

  const terminalOrAlreadyFailed = new Set(['canceled', 'unpaid', 'incomplete', 'incomplete_expired']);
  const nextStatus = terminalOrAlreadyFailed.has(existing.status) ? existing.status : 'past_due';
  await asyncDb
    .prepare(
      `
      UPDATE billing_subscriptions
      SET status = ?, updated_at = CURRENT_TIMESTAMP
      WHERE stripe_subscription_id = ?
    `,
    )
    .run(nextStatus, stripeSubscriptionId);
  return getBillingSubscriptionByStripeId(stripeSubscriptionId);
}

export async function markStripeCustomerSubscriptionsCanceled(
  stripeCustomerId: string,
  canceledAt?: string | Date | null,
): Promise<number> {
  const result = await asyncDb
    .prepare(
      `
      UPDATE billing_subscriptions
      SET
        status = 'canceled',
        canceled_at = COALESCE(canceled_at, ?),
        updated_at = CURRENT_TIMESTAMP
      WHERE stripe_customer_id = ?
        AND status <> 'canceled'
    `,
    )
    .run(isoOrNull(canceledAt ?? new Date()), stripeCustomerId);
  return result.changes;
}

export async function upsertStripeSubscriptionState(
  input: UpsertStripeSubscriptionInput,
): Promise<BillingSubscriptionState> {
  return asyncDb.transaction(async () => {
    const customer = await upsertBillingCustomer(input.userId, input.stripeCustomerId);

    await asyncDb
      .prepare(
        `
        INSERT INTO billing_subscriptions (
          user_id,
          billing_customer_id,
          stripe_customer_id,
          stripe_subscription_id,
          status,
          plan_key,
          stripe_price_id,
          current_period_start,
          current_period_end,
          trial_start,
          trial_end,
          cancel_at_period_end,
          canceled_at,
          updated_at
        )
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, CURRENT_TIMESTAMP)
        ON CONFLICT(stripe_subscription_id) DO UPDATE SET
          user_id = excluded.user_id,
          billing_customer_id = excluded.billing_customer_id,
          stripe_customer_id = excluded.stripe_customer_id,
          status = excluded.status,
          plan_key = excluded.plan_key,
          stripe_price_id = excluded.stripe_price_id,
          current_period_start = excluded.current_period_start,
          current_period_end = excluded.current_period_end,
          trial_start = excluded.trial_start,
          trial_end = excluded.trial_end,
          cancel_at_period_end = excluded.cancel_at_period_end,
          canceled_at = excluded.canceled_at,
          updated_at = CURRENT_TIMESTAMP
      `,
      )
      .run(
        input.userId,
        customer.id,
        input.stripeCustomerId,
        input.stripeSubscriptionId,
        input.status,
        input.planKey,
        input.stripePriceId ?? null,
        isoOrNull(input.currentPeriodStart),
        isoOrNull(input.currentPeriodEnd),
        isoOrNull(input.trialStart),
        isoOrNull(input.trialEnd),
        input.cancelAtPeriodEnd ? 1 : 0,
        isoOrNull(input.canceledAt),
      );

    const state = await getBillingSubscriptionForUser(input.userId);
    if ('customer' in state) return state;
    throw new Error('Failed to persist billing subscription.');
  })();
}
